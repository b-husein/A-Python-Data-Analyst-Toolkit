
# Two types of numbers as data types:

"""
- integers

- float

"""

# int - whole numbers which we can use to perform math operations 

print(3 + 3)
print(3 - 3)
print(3 * 3)
print(3 / 3)

# we can also use type function here;
# which will tell us what data type we are working with.

print(type(6))

# float takes up much more memory than int; 

# Another math operation we need to pay attention to is called
# power off; and it's marked with double multiplier **

print(type(2 ** 2)) 

# also double divider

print(5 // 4)

# and modulo

print(5 % 4)